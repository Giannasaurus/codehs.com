/** Write a function favoriteColors that takes one parameter: colors (an array of three strings).
    The function should return a string in the format: “My favorite colors are [color1], [color2], and [color3].” */

function favoriteColors(colors) {
    return `My favorite colors are ${colors[0]}, ${colors[1]}, and ${colors[2]}.`
}

