/** Create a function isVegetarian that takes one parameter: a string dishName.
    The function should return:
    - true if the dish is “salad”, “pasta”, or “tofu stir fry”
    - false for any other dish */

function isVegetarian(dishName) {
	return dishName === "salad" ||
	       dishName === "pasta" ||
	       dishName === "tofu stir fry";
}

